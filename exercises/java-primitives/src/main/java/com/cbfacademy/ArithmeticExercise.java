package com.cbfacademy;

public class ArithmeticExercise {
    
        float AddFloat(Float operand1, Float operand2) {
            float add = operand1 + operand2;
            return add;

        }

        float SubtractFloat(float operand1, float operand2) {
            float subtract = operand1 - operand2;
            return subtract;

        }

        float MultiplyFloat(float oparand1, float operand2) {
            float multiply = oparand1 * operand2;
            return multiply;

        }
        float DivideFloat(float operand1, float operand2) {
            float divide = operand1 / operand2;
            return divide;

        }

    }



    

